# 1) rbenv (ruby versions & their associated gems)
# 2) To make Homebrew work, put it at the front of the path
# so brew packages are used over Mac OS defaults (e.g. git)
# 3) global NPM packages (e.g. Grunt, Yeoman)
command -v rbenv &> /dev/null && export PATH="$(rbenv prefix)/bin:$PATH"

export PATH="/usr/local/bin:/usr/local/share/npm/bin:$PATH"
